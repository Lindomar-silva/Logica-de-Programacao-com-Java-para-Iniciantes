package cursos.capitulo3;

public class TiposNumericos {

	@SuppressWarnings("unused")
	public static void main(String[] args) {

		// NUMEROS TIPOS INTEIROS
		Byte numeroByte = 127; // -128 até 127
		Short numeroShort = -32768; // -32768 até 32767
		Integer numeroInteger = -2147483648; // -2147483648 até 2147483647
		Long numeroLong = 9223372036854775807L; // -9.223.372.036.854.775.808 até 9.223.372.036.854.775.807

		// NÚMEROS COM CASAS DECIMAIS
		Float numeroFloat = 1.0f; // -3.40292347E+38 a +3.40292347E+38
		Double numeroDouble = 1.0; // -1.79769313486231570E+308 a +1.79769313486231570E+308
	}
}
